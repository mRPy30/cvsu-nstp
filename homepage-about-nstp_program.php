    <h3>The course mandated by Republic Act No. 9163, otherwise known as the National Service Training Act of 2001, aims to enhance the civic consciousness of the students “by developing the ethics of service and patriotism” while undergoing Civic Welfare Training Service (CWTS). NSTP1 covers topics through big sessions in campus that will tap on the students’ enthusiasm and idealism for nation-building, leadership and civic involvement. Combining active reflection in a creative dynamic learning environment, it prepares the students into actual community service in NSTP 2.</h3>
