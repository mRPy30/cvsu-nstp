    <h2>National Service Training Program 2</h2>
    <h3>This course is the natural follow through of the Civic Welfare Training Service (CWTS) the students underwent in NSTP 1. It includes the programs and activities highlighted by the community service/immersion that are contributory to the welfare and the betterment of the life of the members of the community. Among the areas where the students can make their contribution through CWTS 2 are education, environment, entrepreneurship, health and safety and the moral development of the members of the community where they render service. It is hoped that this course will point them to a clearer life-long engagement in service and volunteerism.</h3>
